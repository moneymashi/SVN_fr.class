import numpy as np
## gabage값을 갖는 10행 5열의 배열 생성..
ar = np.empty((10,5))
print(ar)
for idx in range(10) :
    ar[idx]=idx
print(ar)    
## 1,3, 5, 7행만 선택..
br = ar[[1,3,5,7]]
print("1,3,5,7행선택",br)
## [0,3] [1,4] 만 선택..
cr = ar[[0,3],[1,4]]
print("선택처리", cr)
print(ar)
## [[0,1]][[:,[1,2]] 
##print("데이터1:",ar[[0,0]:])
print("데이터2:",ar[3:,[1,2]] )
