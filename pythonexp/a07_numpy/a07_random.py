import numpy as np
## np.random.normal() : 데이터 1개를 랜덤으로 생성..
print(int(np.random.normal()))
## random.normal(래덤으로나타날데이터건수)
print(np.random.normal(size=5))
## 듀플표시로 행열단위로 random을 처리 가능 size=(행,열)
print(np.random.normal(size=(2,3)))
## seed는 랜덤데이터가 동일한 내용을 한번실행한 후, 계속 나타나야 할
## 필요성이 있을 때, 고유번호을 등록하고 실행마다 같은 내용을 처리한다.

np.random.seed(seed=100)
print("시드 설정 후",np.random.normal(size=5))
print("시드 설정 후",np.random.normal(size=5))




