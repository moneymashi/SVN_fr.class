import numpy as np
ar = np.array([90,40,30,75])

ar.sort() ## 정렬 처리..
print(ar)
## 데이터의 상위에 소속된 데이터, 하위에 소속된 데이터 ..
print("크기:", len(ar))
print("크기에서 중간(int):",int( 0.5*len(ar) ) )
##  index 범위 데이터 접근..   배열[시작(0~~):마지막(1~~)]
print("1번~2번째:",ar[0:2])
## 하위 50%
print("하위50%:",ar[0: int( 0.5*len(ar) )])
print("상위50%:",ar[int( 0.5*len(ar)*-1 ):])



