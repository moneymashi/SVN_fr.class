package jspexp.z02_vo;

public class Student {
	private String name;
	private int hakno;
	private int classno;
	private int kor;
	private int eng;
	private int mat;
	private int tot;
	public Student() { }
	public Student(String name, int hakno, int classno, int kor, int eng, int mat) {
		super();
		this.name = name;
		this.hakno = hakno;
		this.classno = classno;
		this.kor = kor;
		this.eng = eng;
		this.mat = mat;
		this.tot = kor+eng+mat;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHakno() {
		return hakno;
	}
	public void setHakno(int hakno) {
		this.hakno = hakno;
	}
	public int getClassno() {
		return classno;
	}
	public void setClassno(int classno) {
		this.classno = classno;
	}
	public int getKor() {
		return kor;
	}
	public void setKor(int kor) {
		this.kor = kor;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMat() {
		return mat;
	}
	public void setMat(int mat) {
		this.mat = mat;
	}
	public int getTot() {
		return tot;
	}
	public void setTot(int tot) {
		this.tot = tot;
	}
}
