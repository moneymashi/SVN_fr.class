package javahomework.a18_yoonyoungju;
public class B01_home_03 {

	public static void main(String[] args) {
	
		
		/*
		 4. 이중 for 문을 이용하여 방정식 4x + 5y = 60 인 
			모든 해를 구하세요  x,y는 10이하 정수 
			 출력형식  : x : @@, y : @@ 
		 */

		for(int x = 1; x <= 10 ; x++){
			
			for( int y = 1; y<=10; y++){
				if((4*x) + (5*y) == 60){
				System.out.println("x :"+ x +",y :"+ y );
				}
			}
		}
	}
}
