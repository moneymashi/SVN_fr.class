/* 1단계 : 
 * 	-맛있는 음식점에 가서 (클래스 생성)
 *      orderFood(가변인자처리)
 *  - 출력내용
 *  	주문하신음식은 : @@
 *  	주문하신음식은 : @@ 가변처리
 *  2단계
 *   - 가게이름을 생성자로 입력받게 처리
 *   - orderFood를 통해서 주문된 음식의 종류에 따라 매핑되어 있는 가격을 계산해서 계산서
 *   	까지 출력처리
 * */
class Restaurant{
	public void orderFood(String ...foods){
		for(String food : foods)
			System.out.println("주문하신 음식은 " + food);
	}
}
public class Homework0421_1_1Step {
	public static void main(String[] args) {
		Restaurant restaurant = new Restaurant();
		restaurant.orderFood("짬뽕","짜장면");
	}
}