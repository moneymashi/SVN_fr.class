package a04_statedment;

public class HW_exp1n2 {
	public static void main(String[] args) {
		// 1) 조건문 if/ if,else
		//	반복문 for / while / do while
		// 2) 10~150 까지의 정수 중 5배수 총합을 구하는 코드 작성
		int sum = 0;
		for(int i=10; i<=150;i++){
			if(i%5==0){
				System.out.print(i+" + ");
			}
			sum+=i;
		}
		System.out.println(" = "+sum);

	}
}

	
	


