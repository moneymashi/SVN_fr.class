package javaexp.a03_operator;

public class A06_argumentExp {
	/* 확인예제 : 새로운 파일 A06_argumentExp.java
	 * Favorite Movie
	 * args => 영화명1 상영년도 영화명2 상영연도
	 * 내가 좋아하는 영화
	 * 영화명 상영 지금으로부터
	 * 영화명 XXX 2017-@@@
	 */
	public static void main(String[] args) {
		System.out.println("★ 내가 좋아하는 영화 ★");
		System.out.println("영화명\t상영\t지금으로부터");
		System.out.println("■■■■■■■■■■■■■■■■■■■■■■■■■■■");
		int no=0;
		for(int i=0 ; i<args.length ; i+=2){
			System.out.println("영화명\t"+args[i]+"\t"+args[i+1]);
			no ++;
		}
		System.out.println("■■■■■■■■■■■■■■■■■■■■■■■■■■■");
		System.out.println("이상 입력하신 영화 갯수 : "+no);
	}
}
