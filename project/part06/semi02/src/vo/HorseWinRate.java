package vo;

public class HorseWinRate {
	private int hnum;
	private int rank;
	private String hname;
	private int totrace;
	private int first;
	private int second;
	private int third;
	private int totprize;
	
	public HorseWinRate() {}
	
	public int gethnum() {
		return hnum;
	}
	public void sethnum(int hnum) {
		this.hnum = hnum;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String gethname() {
		return hname;
	}
	public void sethname(String hname) {
		this.hname = hname;
	}
	public int getTotrace() {
		return totrace;
	}
	public void setTotrace(int totrace) {
		this.totrace = totrace;
	}
	public int getFirst() {
		return first;
	}
	public void setFirst(int first) {
		this.first = first;
	}
	public int getSecond() {
		return second;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	public int getThird() {
		return third;
	}
	public void setThird(int third) {
		this.third = third;
	}
	public int getTotprize() {
		return totprize;
	}
	public void setTotprize(int totprize) {
		this.totprize = totprize;
	}
	
	
}
