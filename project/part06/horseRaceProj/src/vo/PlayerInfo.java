package vo;

public class PlayerInfo {
	private int rank;
	private String pid;
	private int mchoice;
	private String pass;
	private String pname;
	private String email;
	private String tel;
	private double totbet;
	private double totBenefit;
	private double totLose;
	private double curMoney;
	private int first;
	private int second;
	private int third;
	
	public PlayerInfo(){}
	
	public PlayerInfo(String pid, int mChoice, String pass, String email, String tel, int totBet, int totBenefit,
			int totLose, int curMoney, int first, int second, int third) {
		this.pid = pid;
		this.mchoice = mChoice;
		this.pass = pass;
		this.email = email;
		this.tel = tel;
		this.totbet = totBet;
		this.totBenefit = totBenefit;
		this.totLose = totLose;
		this.curMoney = curMoney;
		this.first = first;
		this.second = second;
		this.third = third;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pId) {
		this.pid = pId;
	}

	public int getMchoice() {
		return mchoice;
	}

	public void setMchoice(int mChoice) {
		this.mchoice = mChoice;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

	public String getPname() {
		return pname;
	}

	public void setPname(String pName) {
		this.pname = pName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public double getTotbet() {
		return totbet;
	}

	public void setTotbet(double totBet) {
		this.totbet = totBet;
	}

	public double getTotBenefit() {
		return totBenefit;
	}

	public void setTotBenefit(double totBenefit) {
		this.totBenefit = totBenefit;
	}

	public double getTotLose() {
		return totLose;
	}

	public void setTotLose(double totLose) {
		this.totLose = totLose;
	}

	public double getCurMoney() {
		return curMoney;
	}

	public void setCurMoney(double curMoney) {
		this.curMoney = curMoney;
	}

	public int getFirst() {
		return first;
	}

	public void setFirst(int first) {
		this.first = first;
	}

	public int getSecond() {
		return second;
	}

	public void setSecond(int second) {
		this.second = second;
	}

	public int getThird() {
		return third;
	}

	public void setThird(int third) {
		this.third = third;
	}


	
}
