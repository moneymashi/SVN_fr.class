package jspexp.a01_basic;

public class Woman {
	String name;
	String telnum;
	String height;
	
	public void setName(String name){
		this.name = name;
	}
	public void setTelnum(String telnum){
		this.telnum = telnum;
	}
	public void setHeight(String height){
		this.height = height;
	}
	public String getName(){
		return name;
	}
	public String getTelnum(){
		return telnum;
	}
	public String getHeight(){
		return height;
	}
}
