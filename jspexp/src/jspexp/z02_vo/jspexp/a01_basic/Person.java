package jspexp.a01_basic;

public class Person {
	private String name;
	private int age;
	private String loc;
	public String getName(){
		return name;
	}
	public int getAge(){
		return age;
	}
	public String getLoc(){
		return loc;
	}
	public void setName(String name){
		this.name = name;
	}
	public void setAge(int age){
		this.age = age;
	}
	public void setLoc(String loc){
		this.loc = loc;
	}
}
